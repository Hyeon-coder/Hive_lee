#include "../libft.h"

int ftstrcmp(char *s1, char *s2)
{
    size_t  i;
    i = 0;
    if (!s1 || !s2)
        return (0);
    while (s1[i] || s2[i])
    {
        if (s1[i] != s2[i])
            return (0);
        i++;
    }
    return (1);
}